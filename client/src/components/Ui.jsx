export function IconBtn({ label, danger, onClick, children }) {
  return (
    <button type="button" className={`icon-btn ${danger ? 'danger' : ''}`} title={label} aria-label={label} onClick={onClick}>
      {children}
    </button>
  );
}

export function Modal({ title, children, footer, onClose }) {
  return (
    <div className="modal-overlay" onMouseDown={(e) => { if (e.target === e.currentTarget) onClose(); }}>
      <div className="modal" role="dialog" aria-modal="true" aria-label={title}>
        <div className="modal-head">
          <h2>{title}</h2>
          <IconBtn label="Close" onClick={onClose}>{icons.x}</IconBtn>
        </div>
        <div className="modal-body">{children}</div>
        {footer && <div className="modal-foot">{footer}</div>}
      </div>
    </div>
  );
}
